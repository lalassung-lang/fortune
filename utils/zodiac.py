"""
별자리·띠 판별 모듈
생년월일 → 태양 별자리 / 12지신 띠 반환
"""

from datetime import date


# ── 별자리 ───────────────────────────────────────────────────────────────────

ZODIAC_SIGNS = [
    ("염소자리", "♑", (1, 1),  (1, 19)),
    ("물병자리", "♒", (1, 20), (2, 18)),
    ("물고기자리","♓", (2, 19), (3, 20)),
    ("양자리",   "♈", (3, 21), (4, 19)),
    ("황소자리", "♉", (4, 20), (5, 20)),
    ("쌍둥이자리","♊", (5, 21), (6, 20)),
    ("게자리",   "♋", (6, 21), (7, 22)),
    ("사자자리", "♌", (7, 23), (8, 22)),
    ("처녀자리", "♍", (8, 23), (9, 22)),
    ("천칭자리", "♎", (9, 23), (10, 22)),
    ("전갈자리", "♏", (10, 23),(11, 21)),
    ("사수자리", "♐", (11, 22),(12, 21)),
    ("염소자리", "♑", (12, 22),(12, 31)),
]


def get_star_sign(birth: date) -> dict:
    """생년월일 → 별자리 이름·기호·날짜 범위 반환"""
    md = (birth.month, birth.day)
    for name, symbol, start, end in ZODIAC_SIGNS:
        if start <= md <= end:
            return {
                "name": name,
                "symbol": symbol,
                "range": f"{start[0]}/{start[1]} ~ {end[0]}/{end[1]}",
            }
    return {"name": "염소자리", "symbol": "♑", "range": "12/22 ~ 1/19"}


# ── 12지신 띠 ─────────────────────────────────────────────────────────────────

CHINESE_ZODIAC = [
    ("원숭이", "🐒"),
    ("닭",    "🐓"),
    ("개",    "🐕"),
    ("돼지",  "🐖"),
    ("쥐",    "🐀"),
    ("소",    "🐂"),
    ("호랑이","🐅"),
    ("토끼",  "🐇"),
    ("용",    "🐉"),
    ("뱀",    "🐍"),
    ("말",    "🐎"),
    ("양",    "🐑"),
]

# 궁합 좋은 띠 (전통 삼합)
COMPATIBLE = {
    "쥐":    ["용", "원숭이"],
    "소":    ["뱀", "닭"],
    "호랑이":["말", "개"],
    "토끼":  ["양", "돼지"],
    "용":    ["쥐", "원숭이"],
    "뱀":    ["소", "닭"],
    "말":    ["호랑이", "개"],
    "양":    ["토끼", "돼지"],
    "원숭이":["쥐", "용"],
    "닭":    ["소", "뱀"],
    "개":    ["호랑이", "말"],
    "돼지":  ["토끼", "양"],
}


def get_animal_sign(birth: date) -> dict:
    """생년월일 → 12지신 띠 이름·이모지·궁합 반환"""
    name, emoji = CHINESE_ZODIAC[(birth.year - 1956) % 12]
    compatible = COMPATIBLE.get(name, [])
    return {
        "name": name,
        "emoji": emoji,
        "compatible": compatible,
        "label": f"{emoji} {name}띠",
    }
